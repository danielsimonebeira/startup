package objeto;

public class StartupDados {
    private String nomeStartup, areaStartup, descricaoStartup;
    private String mesAnoCriacaoStartup;

    public String getNomeStartup() {
        return nomeStartup;
    }

    public void setNomeStartup(String nomeStartup) {

        this.nomeStartup = nomeStartup;
    }

    public String getAreaStartup() {

        return areaStartup;
    }

    public void setAreaStartup(String areaStartup) {

        this.areaStartup = areaStartup;
    }

    public String getDescricaoStartup() {

        return descricaoStartup;
    }

    public void setDescricaoStartup(String descricaoStartup) {

        this.descricaoStartup = descricaoStartup;
    }

    public String getMesAnoCriacaoStartup() {

        return mesAnoCriacaoStartup;
    }

    public void setMesAnoCriacaoStartup(String mesAnoCriacaoStartup) {
        this.mesAnoCriacaoStartup = mesAnoCriacaoStartup;
    }

}
